    private void jComboBoxProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxProdutoActionPerformed
        // TODO add your handling code here:

        String produto = jComboBoxProduto.getSelectedItem().toString();

        switch (produto) {
            case "Tilápia":
                jTextFieldPrecoUnitario.setText("R$ 39.90");
                break;
            case "Tambaqui":
                jTextFieldPrecoUnitario.setText("R$ 49.90");
                break;
            case "Pacu":
                jTextFieldPrecoUnitario.setText("R$ 49.90");
                break;
            case "Dourado":
                jTextFieldPrecoUnitario.setText("R$ 89.90");
                break;
            case "Camarão":
                jTextFieldPrecoUnitario.setText("R$ 99.90");
                break;
            default:
                jTextFieldPrecoUnitario.setText("");
                break;
        }
    }//GEN-LAST:event_jComboBoxProdutoActionPerformed

    private void jSpinnerQuantidadeStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerQuantidadeStateChanged

        try {
            int quantidade = Integer.parseInt(jSpinnerQuantidade.getValue().toString());

            if (quantidade != 0) {
                double precoUnitario = Double.parseDouble(jTextFieldPrecoUnitario.getText()
                        .replace("R$ ", "")
                        .replace(",", "."));
                double totalProduto = quantidade * precoUnitario;
                jTextFieldTotalProduto.setText(String.valueOf(totalProduto));
            } else {
                limparCampos();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Selecione um produto1!!");
            e.printStackTrace();
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Selecione um produto2!!");
            e.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Selecione um produto3!!");
            e.printStackTrace();
        }


    }//GEN-LAST:event_jSpinnerQuantidadeStateChanged

