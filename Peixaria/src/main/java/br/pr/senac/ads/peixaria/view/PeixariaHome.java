    private void jButtonVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVenderActionPerformed
        // TODO add your handling code here:

        if (!jComboBoxProduto.getSelectedItem().equals("Nenhum...")) {
            double totalProduto = Double.parseDouble(jTextFieldTotalProduto.getText().replace("R$ ", ""));
            caixa = Double.parseDouble(jTextFieldCaixa.getText().replace("R$ ", ""));

            caixa = caixa + totalProduto;

            jTextFieldCaixa.setText(String.valueOf(caixa));
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto!!");
        }
        
        PeixariaController controlarPeixaria = new PeixariaController();
        
        controlarPeixaria.setProduto(jComboBoxProduto.getSelectedItem().toString());
        controlarPeixaria.setPrecoUnitario(jTextFieldPrecoUnitario.getText().replace("R$ ", ""));
        controlarPeixaria.setQuantidade(jSpinnerQuantidade.getValue().toString());
        controlarPeixaria.setPrecoTotal(jTextFieldTotalProduto.getText());
        controlarPeixaria.setCaixa(jTextFieldCaixa.getText().replace("R$ ", ""));
        
        try {
            controlarPeixaria.salvar(
                    controlarPeixaria.getProduto(),
                    controlarPeixaria.getPrecoUnitario(),
                    controlarPeixaria.getQuantidade(),
                    controlarPeixaria.getPrecoTotal(),
                    controlarPeixaria.getCaixa());
                    JOptionPane.showMessageDialog(this, "Venda salva com sucesso!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Não foi possível salvar a venda!" + e.getLocalizedMessage());
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Erro no Código fonte!" + e.getLocalizedMessage());
        }
        
        limparCampos();
        listarBanco();
        jSpinnerQuantidade.setValue(0);
    }//GEN-LAST:event_jButtonVenderActionPerformed

